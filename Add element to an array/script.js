const guestList = ['Eleven', 'Dustin', 'Lucas', 'Will'];

// Add new names to the end using push()
guestList.push('Mike', 'Max');

// Add new names to the beginning using unshift()
guestList.unshift('Nancy', 'Jonathan');

console.log(guestList);
// Outputs: ['Nancy', 'Jonathan', 'Eleven', 'Dustin', 'Lucas', 'Will', 'Mike', 'Max']
